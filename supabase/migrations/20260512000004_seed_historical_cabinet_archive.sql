select
  member_seed.name,
  member_seed.role,
  member_seed.category,
  member_seed.display_order,
  null,
  null,
  member_seed.college,
  null,
  null,
  null,
  null,
  null,
  cabinet_years.id
from member_seed
join public.cabinet_years
  on cabinet_years.slug = member_seed.slug
where not exists (
  select 1
  from public.cabinet_members
  where cabinet_members.cabinet_year_id = cabinet_years.id
    and lower(trim(cabinet_members.name)) = lower(trim(member_seed.name))
    and lower(trim(cabinet_members.role)) = lower(trim(member_seed.role))
);
